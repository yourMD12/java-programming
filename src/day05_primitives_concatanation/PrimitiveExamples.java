package day05_primitives_concatanation;

public class PrimitiveExamples {
    public static void main(String[]args){
        //****WHOLE NUMBERS/INTEGER TYPES******
        byte byteValue = 100; //FROM -128 TO 127
        short shortValue =3200;
        int intValue = 1238384348;
        long longValue = 383272729L; //FOR LONG NUMBER WE NEED TO ADD L
        //*****DECIMAL NUMBERS/FLOATING POINT TYPES*****
        float floatValue = 2356.554F; //ALWAYS NEED TO ADD F TO FLOAT
        double doubleValue = 5447635.254576; //U CAN ADD D BUT IT'S OPTIONAL
        //*****CHARACTER TYPE*****
        char charValue = 'f'; //ONLY ONE CHARACTER OR NUMBER!!!!
        //******BOOLEAN true or false*****
        boolean booleanValue = true; //ONLY TRUE OR FALSE,NOTHING ELSE
        System.out.println(byteValue);
        System.out.println(shortValue);
        System.out.println(intValue);
        System.out.println(longValue);
        System.out.println(floatValue);
        System.out.println(doubleValue);
        System.out.println(charValue);
        System.out.println(booleanValue);







    }
}
