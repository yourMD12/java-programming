package day24_loops;

public class WhileLoopsApples {
    public static void main(String[] args) {

        int apples = 1;
        //until 10
        //apples <=10 "eating an apple 1

        while(apples <= 10){
            System.out.println("eating an apple 1");
            apples++;
        }
        System.out.println("no more apples :(");


    }
}
